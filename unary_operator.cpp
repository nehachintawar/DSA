//increment
#include <iostream>
using namespace std;

int main()
{
    int a = 10;
//post increment
    int b = a++; //kam ; update
    cout << "b = " << b << endl;//10
    cout << "a = " << a << endl;//11
    return 0;// integer valune return 

    //pre increment
    //++a update; kam
}

